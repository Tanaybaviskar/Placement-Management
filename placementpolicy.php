            <p>Students are required to register with the IIITDMJ Placemnt Cell. Only registered students are eligible to participate in Placement Cell activity.

The Day-1 company on campus status will be awarded to all the mass Hirer / Giant recruiters when they asked for the same.

Placement Cell will declare the placement session open in the seventh semester; Day-1 participation is deemed compulsary for all the registered students. External internship students may inform the organization where they are doing the internship and need to participate.

Students on international internship may not be able to participate in placement activity during their internship. However attempts will be made by placement officer to conduct the interview of that student via Skype if HR of that company agrees for the same.

Students are allowed to avail the opportunity to participate in campus placement as many times unless he/she secured first job.

For the placement there are two categories: Category A: Package >= 5.5 lakhs (Dream job) Category B: Package < 5.5 lakhs.
<br><br>
Students having a job in category A are allowed to sit in any other company of category A if :

80% of the eligible batch mates /branch got placed or
Company is giving an offer which is 50% more than their earlier received package or
Student may declare any company of his / her interest as Dream Company for one attempt.
Students having a job in category B are allowed to sit in any other company of category B if :

70% of the eligible batch mates / branch got placed or
Company is giving an offer which is 40% more than their earlier recieved package or
Student may declare any company of his / her interest as Dream Company for one attempt.
In any case, number of successful attempts in each category is limited to two until 90% of the eligible candidates are placed. However Chairman, Placement Cell may allow students to appear for placement as & when required depending on the situation.

In case of company not disclosing its CTC, The Placement Chairperson will have the consensus of the concerned peoples and come up with the expected CTC (on the basis of communication, company's reputation, other college visit etc.) and then according to above policy, placement cell should allow the students coming under it. Final decision will be taken by Placement Chairperson.
<br><br>
The student who got the PPO after the internship from a company will be considered to have a job from the campus and their name will appear in the selection list of Placement cell.

Students are required to follow Code of Conduct. The Placement Cell shall de-register a student who has failed to follow Code of Conduct and specific instructions of the particular event. Following procedure will be followed for re-registration:-

Paying re-registraion fee of Rs. 1000/- for the first time and Rs. 2000/- for the second incident
Students will not be allowed to re-register after second incident i.e. debarred from Placement activities in future. The decision to accept re-registration is vested as the discretion of Chairman, Placement Cell
The student is required to be semiformal / formal in dressing for being accepted in the Placement Office or any activities related to Placement Cell.

